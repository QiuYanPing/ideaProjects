package com.qyp.chat.exception.enums;

public enum ExceptionEnum {
    UNAUTH(404,"权限不足");

    private Integer code;
    private String msg;

    ExceptionEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
